/*
	This program provides
	the three example implementation
	of trigraph characters.

*/

/*	Trigraph characters used
	1).	[
	2).	]
	3).	\
*/

//-------------------- Real program starts from here -----------//

#include <stdio.h>
int main()
{
	// This will print [
	printf("??(");

	// This will print ]
	printf("??)");

	// This will not print \ (Backslash)
	//	printf("??/");

	// To print \ (Backslash) you need to use escape characters
	printf("\\");

	return 0;
}

